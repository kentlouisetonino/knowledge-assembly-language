/*
  Zero Values
    - If variables has no initial value, they are given a zero value.
    
    0       : For numeric types.
    false   : For the boolean type.
    ""      : For strings.
*/

package main

import "fmt"

func main() {
  var i int;
  var b bool;
  var s string;

  fmt.Println("i: ", i);
  fmt.Println("b: ", b);
  fmt.Println("s: ", s);
}

